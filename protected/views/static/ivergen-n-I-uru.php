			<h4 class="h4-vad h4-ap-in">ACCIÓN</h4>
			<p class="p-vad">Antiparasitario interno y externo para bovinos y ovinos,
que combate parásitos gastrointestinales, pulmonares,
sarna, piojos, ura y miasis. Solución inyectable de
Ivermectina y Nitroxinil.</p>
		 
			<h4 class="h4-vad h4-ap-in">COMPOSICIÓN</h4>
			<p class="p-vad">Nitroxinil, 34 g; Ivermectina, 1 g; Excipientes c.s.p, 100 ml.</p>
		 
			<h4 class="h4-vad h4-ap-in">INDICACIÓN</h4>
			<p class="p-vad"><strong>Parásitos Internos:</strong> Formas adultas y larvarias de
Fasciola hepática y parásitos gastrointestinales y
pulmonares: Haemonchus contortus, placei y spp,
Ostertagia ostertagi y spp (incluye formas L4 inhibidas
o hipobióticas), Trichostróngylus spp, Cooperia spp,
Nematodirus spp, Oesophagostomum radiatum y spp,
Trichuris vulpis y spp, Chabertia spp, Bunostomum spp,
Strongyloides spp, Protostróngylus spp y Dictyocaulus
vivíparus y spp. <br/>
<strong>Parásitos Externos:</strong> Larvas de insectos: Dermatobia
hominis, Hypoderma bovis, Hypoderma lineatum y Oestrus
ovis. Piojos: Linognatus vituli, Haematopinus eurysternus y
Solenoptes capillatus. Sarna en bovinos: Psoroptes bovis
y Sarcoptes scabiei var. Bovis.</p>
			
			<h4 class="h4-vad h4-ap-in">Dosis y vía de administración</h4>
			<p class="p-vad"><strong>Bovinos:</strong> Fasciola hepática. Adultos, dosis básica de 1 cc
cada 50 kg de peso (6.8 mg/kg de nitroxinil y 200 mcg/
kg de ivermectina ). La dosis básica puede ser elevada a
1.5 cc cada 50 kg de peso (10mg/kg de nitroxinil y 300
mcg/kg de ivermectina ) a los efectos de lograr aumentar
la eficacia sobre formas de 6 a 8 semanas. Haemonchus
placei, Oesophagostomum radiatum, Bunostomum
phlebotomum; 1.5 cc cada 50 kg de peso. <br/>
<strong>Ovinos:</strong> Fasciola hepática. Adultos, dosis básica de 1 cc
cada 50 kg de peso ( 6.8 mg/kg de Nitroxinil y 200 mcg/
kg de Ivermectina ). La dosis básica puede ser elevada a
1.5 cc cada 50 kg de peso ( 10 mg/kg de Nitroxinil y 300
mcg/kg de Ivermectina ) a los efectos de lograr aumentar
la eficacia sobre formas de 6 a 8 semanas. Haemonchus
contortus, Oesophagostomum venulosum, Bunostomum
trigonocephalum; 1.5 cc cada 50 kg de peso.
Antiparasitarios
Internos
Administrar por inyección subcutánea en zonas de piel
laxa. No aplicar más de 5 cc por punto de inoculación.</p>
			
